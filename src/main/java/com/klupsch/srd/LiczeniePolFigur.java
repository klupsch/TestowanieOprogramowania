package com.klupsch.srd;

public class  LiczeniePolFigur {

    public double prostokat(int a, int b){
        return a*b;


    }
    public double kwadrat (int a, int b){
        return a*b;


    }
    public double kolo(int a){
        return a*a*Math.PI;


    }
    public double trapez (int a, int b, int h){
        return (a+b)*h/2;


    }
}
